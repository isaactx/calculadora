
package Calcjava;

public class Calculadora {
    public double soma (double a, double b){
        return a + b;
    }
    public double sub (double a, double b) {
        return a - b;
    }
    public double mult (double a, double b) {
        return a * b;
    }
    public double div (double a, double b) {
        return a / b;
    }
}
